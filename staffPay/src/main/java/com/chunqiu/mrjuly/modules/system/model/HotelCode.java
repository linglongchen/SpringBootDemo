package com.chunqiu.mrjuly.modules.system.model;

import com.chunqiu.mrjuly.common.persistence.DataEntity;

public class HotelCode extends DataEntity<HotelCode, Long> {
    private static final long serialVersionUID = 1L;

    private Long id;
    private String hotelCode;//酒店的前缀编码
    private String hotelName;//酒店名称
    private String uniqueEncoding;//编码名称

    private int type;

    private Long codeId;

    private String  idList;//批量归入前缀存储id


    public String getIdList() {
        return idList;
    }

    public void setIdList(String idList) {
        this.idList = idList;
    }

    public Long getCodeId() {
        return codeId;
    }

    public void setCodeId(Long codeId) {
        this.codeId = codeId;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getUniqueEncoding() {
        return uniqueEncoding;
    }

    public void setUniqueEncoding(String uniqueEncoding) {
        this.uniqueEncoding = uniqueEncoding;
    }

    @Override
    public Long getId() {
        return id;
    }

    @Override
    public void setId(Long id) {
        this.id = id;
    }

    public String getHotelCode() {
        return hotelCode;
    }

    public void setHotelCode(String hotelCode) {
        this.hotelCode = hotelCode;
    }

    public String getHotelName() {
        return hotelName;
    }

    public void setHotelName(String hotelName) {
        this.hotelName = hotelName;
    }
}
